package control_statement;

public class FOR_STUDY {

	public static void main(String[] args) {
		 //OBJECTIVE:I WANT TO PRINT DEEPAK 12 TIMES
		//FROM WHERE TO START===>INITISALISATION
		//CONDITON==============>TO SARTISFY INITIALISATION AND OBJECTIVE//
		//1..2..3..4..5..15=====>INCREAMENTING
		//15..14..13..12..1=====>DECREMENTING
		
		for(int i=1;i<=50;i++)
		{
			System.out.println(i);//directly used variable in the printing system without double quote//
		}
		
		for(int j=15;j>=1;j--) 
		{
			System.out.println("deepak");
			}
	}

}
